package Loops;

public class WhileLoops {

	public static void main(String[] args) {
		
		int i =2; //  while loop check any condition before running any stsatment true or not
		
		while(i<=10) // no need semicolon because it's no do while loop
		{
			System.out.println(i); // will go 1 to 10 
			//i++; // increment by 1 and stop at 10
			i+=2; // will increment by 2 
		}
			System.out.println("Program Stoped"); // it will print in the end
	}

}
